package fr.fourmond.jerome.ontology;

public class TreeOntologyException extends Exception {
	private static final long serialVersionUID = 1L;

	public TreeOntologyException() { super(); }

	public TreeOntologyException(String message) { super(message); }
	
	public TreeOntologyException(Throwable cause) { super(cause); }
	
	public TreeOntologyException(String message, Throwable cause) { super(message, cause); }
}
